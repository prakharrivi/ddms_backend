var express = require('express')
var router = express.Router()
const auth = require('../../middleware/auth')
//  var mailsender = require('../../Routes/sendMail');
var bodyParser = require('body-parser')

var RequestdocumentService = require('../../services/document/requestdocument_service')
var Constant = require('../../Constant/Constant')

router.post('/getrequest', async function (req, res) {
  RequestdocumentService.getrequest(req)
    .then(function (data) {
      if (data) {
        res.status(200).send(data)
      } else {
        res.status(204).send(Constant.noRecordMessage)
      }
    })
    .catch((err) =>
      res.status(500).send({
        respCode: Constant.respCode500,
        respMessage: err.message,
      }),
    )
})

