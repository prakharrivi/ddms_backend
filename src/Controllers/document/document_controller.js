var DocumentService = require('../../services/document/documnet_service')
var Constant = require('../../Constant/Constant')

router.post('/addfilelock', async function (req, res) {
  DocumentService.addfilelock(req)
    .then(function (data) {
      if (data) {
        res.status(200).send(data)
      } else {
        res.status(204).send(Constant.noRecordMessage)
      }
    })
    .catch((err) =>
      res.status(500).send({
        respCode: Constant.respCode500,
        respMessage: err.message,
      }),
    )
})

